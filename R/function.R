#' followers
#'
#' @param x any integer value
#'
#' @return the same value and the two following
#' @export
#'
#' @examples followers(3)


followers <- function(x) {
y <- c(x, x+1, x+2)
return(y)
}


#' half
#'
#' @param z is num
#'
#' @return half of z
#' @export
#'
#' @examples half(10)

half <- function(z) {
  h <- z/2
  return(h)
}

